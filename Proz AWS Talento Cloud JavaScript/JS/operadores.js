// Operadores em JS

// Os operadores aritméticos (soma, subtração, multiplicação, divisão e módulo) 
// e relacionais (maior, menor, maior ou igual, menor ou igual) 
// são exatamente iguais em JS e Python. Revisemos eles rapidamente:

//  Operação                              Operador

//    Soma                                    +

//    Subtração                               -

//    Multiplicação                           *

//    Divisão                                 /

//    Modulo                                  %

//    Maior que                               >

//    Menor que                               <

//    Maior ou Igual que                      >=

//    Menor ou igual que                      <=

// Já os operadores lógicos são um pouco diferentes em JS:

// Operação          Operador(Python)          Operador(JavaScript)

// Conjução          and                       &&

// Dsjunção          or                        ||

// Negação           not ou !                  !

// A seguir, temos três exemplos usando cada um dos operadores lógicos em JS 
// e o resultado das sentenças lógicas


(10 > 5) && (2 >= 7)

// O resultado é 'false'


(10 > 5) || (2 >= 7)

// O resultado é 'true'


!(10 > 5)

// O resultado é 'false'